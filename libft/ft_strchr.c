#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	char	*tmp;

	tmp = (char *)s;
	if (tmp[0] == c)
		return (tmp);
	while (*tmp++)
	{
		if (*tmp == (char)c)
			return (tmp);
	}
	return (0);
}
