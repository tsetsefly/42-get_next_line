#include "libft.h"

char		*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	if (!(str = ft_strnew(ft_strlen(s1) + ft_strlen(s2))))
		return (NULL);
	str = ft_strncpy(str, s1, ft_strlen(s1));
	return (ft_strncat((char *)str, s2, ft_strlen(s2)));
}
