#include "libft.h"

char	*ft_strncat(char *dest, const char *src, int nb)
{
	int	i;
	int	dest_len;

	dest_len = ft_strlen(dest);
	i = 0;
	if (nb < 0)
		nb = ft_strlen(src);
	while (src[i] && i < nb)
	{
		dest[dest_len + i] = src[i];
		i++;
	}
	dest[dest_len + i] = '\0';
	return (dest);
}
